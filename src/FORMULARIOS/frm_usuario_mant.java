    void mostrar(){
    try{
     int fila=jTable1.getSelectedRow();
        txtid_codusu.setText(this.jTable1.getValueAt(fila, 0) + "");
        txtc_nomusu.setText(this.jTable1.getValueAt(fila, 1) + "");
        txtc_passusu.setText(this.jTable1.getValueAt(fila, 2)+"");
        String tipo=this.jTable1.getValueAt(fila, 3)+"";
        String estado=this.jTable1.getValueAt(fila, 4)+"";

        if(tipo.equals("GES")){
        cbo_tipusu.setSelectedItem("GES - GESTION");
        }else{
        cbo_tipusu.setSelectedItem("ADM - ADMINISTRADOR");
        }

        if(estado.equals("ACT")){
        cbo_estusu.setSelectedItem("ACT - ACTIVO");
        }else{
        cbo_estusu.setSelectedItem("INA - INACTIVO");
        }
    }catch(Exception e){
    JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }

    void cargar(){
    try{
    Usuario usu=new Usuario();
    ArrayList lista=new ArrayList();
    lista=usu.listado_usuarios();

    DefaultTableModel m=(DefaultTableModel)jTable1.getModel();
    m.setNumRows(lista.size());

    for(int i=0;i<lista.size();i++){
    usu=(Usuario)lista.get(i);
    m.setValueAt(usu.getId_codusu(), i, 0);
    m.setValueAt(usu.getC_nomusu(), i, 1);
    m.setValueAt(usu.getC_passusu(), i, 2);
    m.setValueAt(usu.getC_tipusu(), i, 3);
    m.setValueAt(usu.getC_codest(), i, 4);
    }this.jTable1.setModel(m);
    }catch(Exception e){
    JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }


