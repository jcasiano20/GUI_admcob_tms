    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed
      try{

        Usuario usu=new Usuario();
        if(accion.equals("new")){
          if(txtid_codusu.getText().trim().isEmpty()){
          JOptionPane.showMessageDialog(null, "Ventana Error","Ingrese Codigo Usuario",JOptionPane.ERROR_MESSAGE);
          return;
          }
          }

          if(txtc_nomusu.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Ventana Error","Ingrese Nombres Usuario",JOptionPane.ERROR_MESSAGE);
          return;
          }

          if(txtc_passusu.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Ventana Error","Ingrese Contrase침a Usuario",JOptionPane.ERROR_MESSAGE);
          return;
          }

        String c_tipusu=(cbo_tipusu.getSelectedItem() + "").substring(0, 3);
        String c_codest=(cbo_estusu.getSelectedItem() + "").substring(0, 3);

        usu.graba_usuario(txtid_codusu.getText(), txtc_nomusu.getText(), txtc_passusu.getText(),
        c_tipusu, c_codest, accion);
        btn_cancelar.setSelected(true);
        btn_grabar.setEnabled(false);
        btn_cancelar.setEnabled(false);
        this.cargar();
      }catch(Exception e){
      JOptionPane.showMessageDialog(null, e.getMessage());
      }

    }//GEN-LAST:event_btn_grabarActionPerformed

